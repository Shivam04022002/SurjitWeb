module.exports = async (ctx) => {
    let created = 0;
    let updated = 0;

    for (const [index, page] of PAGES.entries()) {
        const existing = await LegalPage.findOne({ slug: page.slug });

        // Never clobber an edited page: for existing rows only keep them in place
        // (and refresh nothing), so an admin's changes survive a re-run.
        const data = existing
            ? {}
            : { ...page, displayOrder: index + 1, status: 'Published' };

        const { doc } = await upsertOne(LegalPage, { slug: page.slug }, data, ctx);

        if (existing) updated += 1; else created += 1;
        logger.info(`  ${existing ? 'kept   ' : 'created'}  ${doc.title} (${doc.slug}) — ${doc.status}`);
    }

    return `${created} created, ${updated} kept`;
};

// Allow `node scripts/migrateLegalPages.js` on its own.
if (require.main === module) {
    const { connect, disconnect } = require('./helpers/db');
    const { MigrationContext } = require('./helpers/upsert');

    (async () => {
        const ctx = new MigrationContext('legal-pages');
        try {
            await connect();
            logger.success('Connected to MongoDB');
            const summary = await module.exports(ctx);
            logger.success(`Legal pages: ${summary}`);
        } catch (err) {
            logger.error(err.message);
            const removed = await ctx.rollback();
            logger.warn(`Rolled back ${removed} newly-created record(s).`);
            process.exitCode = 1;
        } finally {
            await disconnect();
        }
    })();
}
